    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
String nama = txtNama.getText();
    String username = txtUsername.getText();
    String password = new String(txtPassword.getPassword());
    String noHp = txtNoHp.getText();

    if (nama.isEmpty() || username.isEmpty() || password.isEmpty() || noHp.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Semua data harus diisi!");
        return;
    }

    try {
        Connection conn = Koneksi.getConnection();

        String sql = "INSERT INTO akun (nama_lengkap, username, password, no_hp, role) VALUES (?, ?, ?, ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);

        pst.setString(1, nama);
        pst.setString(2, username);
        pst.setString(3, password);
        pst.setString(4, noHp);
        pst.setString(5, "pemilik");

        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "Data pemilik berhasil ditambahkan.");

        tampilData();
        kosongkanForm();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal menambah data: " + e.getMessage());
    }
    }//GEN-LAST:event_btnTambahActionPerformed

