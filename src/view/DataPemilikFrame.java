    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
    String id = idPemilik;
    String nama = txtNama.getText();
    String username = txtUsername.getText();
    String password = new String(txtPassword.getPassword());
    String noHp = txtNoHp.getText();

    if (id.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Pilih data yang ingin diedit terlebih dahulu!");
        return;
    }

    try {
        Connection conn = Koneksi.getConnection();

        String sql = "UPDATE akun SET nama_lengkap = ?, username = ?, password = ?, no_hp = ? WHERE id_akun = ?";
        PreparedStatement pst = conn.prepareStatement(sql);

        pst.setString(1, nama);
        pst.setString(2, username);
        pst.setString(3, password);
        pst.setString(4, noHp);
        pst.setString(5, id);

        pst.executeUpdate();

        JOptionPane.showMessageDialog(this, "Data pemilik berhasil diedit.");

        tampilData();
        kosongkanForm();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal mengedit data: " + e.getMessage());
    }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
    String id = idPemilik;

    if (id.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus terlebih dahulu!");
        return;
    }

    int konfirmasi = JOptionPane.showConfirmDialog(
        this,
        "Yakin ingin menghapus data ini?",
        "Konfirmasi",
        JOptionPane.YES_NO_OPTION
    );

    if (konfirmasi == JOptionPane.YES_OPTION) {
        try {
            Connection conn = Koneksi.getConnection();

            String sql = "DELETE FROM akun WHERE id_akun = ?";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.setString(1, id);

            pst.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data pemilik berhasil dihapus.");

            tampilData();
            kosongkanForm();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + e.getMessage());
        }
    }
    }//GEN-LAST:event_btnHapusActionPerformed

